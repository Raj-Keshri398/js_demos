// while loop

let index = 0;
while (index <= 10) {
    //console.log(`Value of index is ${index}`);
    index = index + 2;
}

// while loop with array value
let MyArray = ['flash', 'iron', 'hulk'];
let arr = 0;
while (arr < MyArray.length) {
    //console.log(`Value is ${MyArray[arr]}`);
    arr++;
}


// Do while loop

let score = 1;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);